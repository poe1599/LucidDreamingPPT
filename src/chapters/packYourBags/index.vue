## 意念的力量
----
### 那麼，意念是什麼?

所謂意念，就是目的、目標，針對特定行動的定向思維。
----
### 思想的力量

實驗在腦中模擬運動，能一定程度內增加肌肉強度
腦中模擬的過程，肌肉發出的電波與實際運動是一樣的
----
### 如何設定意念

1. **遣詞用字是關鍵**
2. **感覺它，看清它**
3. **期待**
4. **把它變成腦中做重要的想法**
----
### 如何設定意念

1. **遣詞用字是關鍵**

      <small>若要達到最大效果，意念應該非常明確。創造簡短有力的描述，重點放在你的慾望上。這項作法一般稱為（自我）「肯定」（affirmations）。</small>
      
      <small>ex：~~哪天我也想來學鋼琴~~</small><br>
      <small>ex：星期二我要開始上鋼琴課，到六月之前我要學會彈奏音階</small>

2. **感覺它，看清它**

      <small>想像或回想一個夢境，回想那個身處其中的感覺。</small>
----
### 如何設定意念
    
3. **期待**

      <small>~~被動地希望清醒夢自動發生~~</small>

      <small>期待自己當晚在夢中醒來</small>    

4. **把它變成腦中做重要的想法**

      <small>在睡前屏除雜念</small>
      
      <small>「我在夢中意識清醒。」</small>
----
### 重點整理
<small>  
- 人的心智並無法分辨想法和行動的差別。因此做清醒夢之前，在腦海模擬預演是很重要的
- 想像你看到自己身處夢中，感受認出這是夢境的興奮
- 在真正做清醒夢之前，先對此懷抱感激之情
- 當你設定好意念，用簡單的語句描述 ex：我在夢中清醒且意識清楚
- 培養想做清醒夢的強烈慾望，並且讓這個慾望成為入睡前腦中最重要的想法  
</small>
