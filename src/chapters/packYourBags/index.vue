## 撰寫夢境日誌
----
### 加強夢境回憶

人們起床後五分鐘內通常就會忘記一半以上的夢境內容，起床後十分鐘內則會把九成的夢都忘光。

寫下夢境是最有效回憶夢境的方法。
----
### 如何撰寫夢境日誌

1. **把日誌擺在床邊**  
      <small>便於醒來後紀錄夢境。</small>
    
2. **日期和時間**      
      <small>紀錄睡覺模式，潛意識做下準備。</small>
    
3. **寫下關鍵字**  
      <small>隨意寫下夢中最重要的時刻作為大綱，再根據這些點來擴充。</small>

----
### 如何撰寫夢境日誌
    
4. **用現在式的語氣書寫**  
      <small>用現在式來書寫和思考可以將你帶回夢境之中，讓你回憶起更多細節。</small>
    
5. **為你的夢境命名**  
      <small>寫下夢境後，回頭為這個夢命名。有助於查找。</small>
    
6. **註解**  
      <small>在固定的區塊標註夢境中你是否清醒。寫下引發你在夢中清醒的事物、你的清醒夢維持多久、以及任何你學到、往後可以幫助你探索清醒夢的有用訣竅。</small>
    
----
### 找出夢境背後的意義

「夢是我們還不知道要提問的問題的解答。」

夢屬於做夢者本身。夢是非常個人、私密的東西。我們眼中的蘋果，對你來說未必是蘋果。
----
### 夢兆，清醒夢的誘發物

反覆出現的夢境元素叫做「夢兆」，是前往清醒夢世界的墊腳石。留意這些在夢中反覆出現的元素，是你在夢中恢復意識的重要途徑。
----
### 找出你的夢兆

從夢境日誌中找出個人的夢兆。

上床前告訴自己：「下次再看到ＸＸＸ，我就知道自己在夢中。」
----
### 發現一個自然世界

累積夢境日誌可以幫助你和夢境的聯繫。

找到自己適合的紀錄方式。
----
### 重點整理
<small>

- 要記住夢境最有效的方式，就是將它們寫下來。
- 夢境包含我們經常錯失的頓悟和洞見，除非把夢境記下來、回頭重讀，才有可能察覺。
- 夢兆基本上就是夢中反覆出現的元素，也是辨認自己正在做夢的絕佳手段。
- 醒來後快速寫下重點，不然很快就會忘光光。
- 抱著好玩有趣的心態來撰寫夢境日誌，這是一本記錄你在另一個次元歷險的書籍。

</small>
