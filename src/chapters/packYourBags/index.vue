## 記住你的夢境
----
### 你是做夢者

- ~~我不做夢~~
- ~~睡得很淺~~
- ~~我不像其他人會做那麼多夢~~
- **我是有意識地在做夢**
----
### 夢是真實的經驗

回憶夢的記憶與回憶真實世界的記憶，在本質上並無區別
----
### 入睡

在睡前立下想要記得夢境的意念
----
### 設定意念

1. **重複（自我）肯定**
    
      <small>入睡前將意念聚焦在簡短明確的句子。<br>ex：我記得我的夢</small>

2. **觀想它**
    
      <small>在腦中想像早上醒來記得夢境的內容，記錄下夢中的細節</small>
    
3. **感受它**
    
      <small>感受自己在鮮明的夢中醒來，接受夢中的各種感官刺激</small>
    
----
### 健康的睡眠習慣

1. **建立睡前儀式**
    
      <small>做一些會讓你放鬆的事<br>ex：沖個熱水澡、閱讀、冥想、畫圖、瑜珈、焚香</small>
    
2. **每晚準時上床**
    
      <small>固定的睡眠周期（slepp cycle）有助於引發清醒夢</small>

----
### 健康的睡眠習慣
    
3. **床、浴室，以及另一個世界**  
      <small>睡眠環境應保持昏暗、舒適且安靜，讓你覺得平靜與安全</small>
    
4. **讓阿嬤為你驕傲**  
      <small>減少攝取刺激物，避免不良影響<br>ex：酒精、菸、大麻和咖啡</small>    
----
### 醒來

1. **慢慢醒來，避免任何動作**  
      <small>當你從睡眠中返還，給自己一點時間去記憶方才所在的夢境。</small>

2. **蒐集夢境碎片**  
      <small>試著從殘留的片段聯想夢境的細節。</small>
    
3. **改變睡姿**  
      <small>緩慢回復作夢時的睡姿，回憶起夢境會比較容易。</small>
----
### 醒來
    
4. **檢查情緒**

      <small>透過檢查情緒回憶夢境。</small>
    
5. **做紀錄**

      <small>寫下你的夢境。</small>    
----
### 重點整理
<small>

- 你早上醒來記得的夢，只是關於夢的記憶，而非夢境本身。
- 夢是我們每晚都會經歷的真實體驗，但由於貧乏的夢境記憶，我們忘了另一個自己也參與其中的完整人生。
- 想記得夢境，必須把這件事當成第一優先並全神貫注。把夢境當成你想要記憶的重要體驗，夢就會如繁花般盛開。
- 練習培養良好的睡眠習慣，例如每晚準時上床，充分休息。
- 在睡前建立意念：「我會記得我的夢。」
- 練習正確的醒來方式：要慢慢醒來，避免任何動作。
- 在試圖做清醒夢之前，每晚至少要記得一個夢，這一點很重要。

</small>
