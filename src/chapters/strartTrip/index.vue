### 瞭解夢境
試圖解釋夢境為何的理論不勝枚舉。
佛洛伊德主張所有的夢境都是人類實現願望的形式。也就是說，夢境的出現是因為我們在日常生活中累積了許多被壓抑的衝突和慾望。夢是我們心靈在「無意識」（unconscious）狀態下試圖解決這些過往衝突的作法。
----
其他主流的觀點：
- **人腦就像電腦**
<br><small>夢是我們組織訊息的方式之一，幫助我們儲存記憶。夢是一種「磁碟重整」的方式。</small>

- **未來的預演**
<br><small>夢是一種安全環境，讓我們可以在此準備、練習即將發生的事。</small>

- **隨機**
<br><small>夢是神經隨機放電的附屬品……大腦前額葉試圖將之組成一條故事線。夢就是胡言亂語。</small>
----
### 夢境體驗
我們似乎沒有把夢境當成人類經驗中的無價之寶，好好地珍視它。

「典型的做夢者在醒來之後完全記不得他前一晚身在何方，就跟失憶的醉鬼沒什麼兩樣。」<br><small>by 羅伯‧摩斯（Robert Moss）—— 有意識地作夢</small>

我們被教導要無視於夢境，把夢境當做無意義的干擾物。
