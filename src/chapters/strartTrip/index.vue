## 夢是什麼?
夢境是日常生活重要的一部份，<br>人們受到夢的啟發及指引。<br>許多驚人的科學發現、無價的發明、文學藝術創作，都可歸功於夢境。
----
### 世人皆夢
不論年齡、種族、宗教信仰、職業、飲食習慣或性取向為何，我們都會做夢，而且每天晚上都是如此。
