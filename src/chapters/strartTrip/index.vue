# 踏上旅程
----
## 嶄新的發現
- 當我們周遊在夢鄉之際，身體基本上處於癱瘓狀態，大腦中的運動神經元此刻靜止不受刺激，身體的肌肉沉睡不動，這是一種正常狀態，稱之為**睡眠麻痺（sleep atonia）**
- 人體中有兩個器官不受這種癱瘓狀態影響：

    - 一是**橫膈膜**，讓我們保持呼吸
    - 另一個是**眼睛**
----
### 用科學證明在睡夢中清醒    
早上八點零七分，沃里斯發現自己在夢境之中。
一旦他意識到自己正在做夢，亦即他的身體在清醒世界中無意識地躺。他馬上進行事先約定好的特定眼球動作——**眼球左右來回轉動八次**——好讓實驗室知道他正在做夢，而且是有意識地這麼做。

腦波圖（EEG）的指數證明他正處於睡眠狀態，但他的大腦卻清醒得組已傳送訊息回實驗室——他正在做清醒夢。
----
### 清醒夢的效益
1. **探險和幻想**
<br><small>既然真實世界的物理定律和社會規範在夢中世界都不存在，夢中的清醒狀態提供做夢者一個最佳的遊樂場。</small>

2. **面對惡夢**
<br><small>如果能在惡夢中「醒來」，你不但擁有扭轉潛意識創造出的恐怖故事的腳本能力，還有力量找出糾纏你的源頭，進而療癒自己</small>

3. **激發創意和靈感**
<br><small>夢境就是讓創造力無拘無束自在奔馳的最理想地點，這裡毫無限制，你幾乎可以創造任何你想要的事物</small>
----
### 清醒夢的效益
4. **具創意的問題解決之道**
<br><small>清醒夢可以做為測試場所，自古人們就會在夢裡尋求指引，夢境也是找出日常生活裡問題解決之道的最佳途徑。</small>

5. **療癒**
<br><small>在清醒夢中，你可以療癒自己身上的許多層面。你可以變得更快樂，也更完整。</small>

6. **認識自我**
<br><small>做清醒夢可以是探索靈魂的絕佳工具，也是你碰觸更深層自我的方式。</small>
